public class DataPointController {

    String id_perolehan;
    String no_kartu;
    String admin;
    String waktu;
    String tanggal_point;
    String tanggal_pasif;

    public String getId_perolehan() {
        return id_perolehan;
    }

    public void setId_perolehan(String id_perolehan) {
        this.id_perolehan = id_perolehan;
    }

    public String getNo_kartu() {
        return no_kartu;
    }

    public void setNo_kartu(String no_kartu) {
        this.no_kartu = no_kartu;
    }

    public String getAdmin() {
        return admin;
    }

    public void setAdmin(String admin) {
        this.admin = admin;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getTanggal_point() {
        return tanggal_point;
    }

    public void setTanggal_point(String tanggal_point) {
        this.tanggal_point = tanggal_point;
    }

    public String getTanggal_pasif() {
        return tanggal_pasif;
    }

    public void setTanggal_pasif(String tanggal_pasif) {
        this.tanggal_pasif = tanggal_pasif;
    }
}
